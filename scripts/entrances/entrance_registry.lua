-- Reverse indexes, built once at load. Do not edit by hand.
-- byId maps each physical tile id -> LIST of rows that own it (usually one; >1 only for
-- the option-gated vanilla/Route-23-Restored pairs). ResolveEntranceRow picks the active
-- one: a gated row wins when its gate code is set, else the ungated (vanilla) row.
REGISTRY = { byId = {}, byPretty = {} }
for token, row in pairs(ENTRANCE_REGISTRY) do
    row.token = token
    REGISTRY.byPretty[row.pretty] = row
    for _, wid in ipairs(row.ids) do
        local bucket = REGISTRY.byId[wid]
        if bucket then
            bucket[#bucket + 1] = row
        else
            REGISTRY.byId[wid] = { row }
        end
    end
end

--- The registry row that a just-entered warp id should reveal, or nil.
--- One owner -> that row. Several -> the gated row whose code is set, else the ungated one.
---@param id integer
---@return table|nil
function ResolveEntranceRow(id)
    local bucket = REGISTRY.byId[id]
    if not bucket then return nil end
    if #bucket == 1 then return bucket[1] end
    local fallback
    for _, row in ipairs(bucket) do
        if row.gate then
            if has(row.gate) then return row end
        else
            fallback = row
        end
    end
    return fallback or bucket[1]
end
