-- Central correlation table between the representations of every randomizable entrance:
--   * region-string token  (slot_data + graph identity, and the table key here)
--   * numeric warp id LIST (what the DataStorage "entered" list carries; a door can
--     have several physical tiles, and entering via ANY of them counts. Empty for
--     reverse-elevator rows: their exit uses a shared interior tile that can't identify
--     the floor, so they are revealed via their coupled forward instead.)
--   * pretty name          (item badge + route hop label)
--   * tab chain / section  (UiHint navigation + temp-highlight)
--   * gate (optional)      (item code that must be set for this row to own its shared
--     id; used only where a vanilla edge and its Route-23-Restored variant reuse a tile.)
--
-- The warp's ER CATEGORY and one-way-ness live on its connect_*_entrance edge in
-- connections.lua, NOT here. Loaded after the graph, before createEntrances().

-- The ER categories. Each has a matching progressive toggle item in items/settings_er.json
-- with codes "er_<cat>" / "er_<cat>_on" / "er_<cat>_off". Kept here for load order (init.lua
-- and entrance_validate.lua read it before their own setup).
